const fs = require("fs");
const {run1, run2} = require("./index.js");

describe("day22 run1", () => {
    test("run", () => {
        const data = [
            "Player 1:",
            "9",
            "2",
            "6",
            "3",
            "1",
            "",
            "Player 2:",
            "5",
            "8",
            "4",
            "7",
            "10"
        ];

        const response = run1(data);

        expect(response).toBe(306);
    });
    test("full run", () => {
        const input = fs.readFileSync(__dirname + "/input.txt", "utf8");
        const data = input.toString().split("\r\n");
        data.splice(-1, 1);

        const response = run1(data);

        console.log(response);
    });
});

